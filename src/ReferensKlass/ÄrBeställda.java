package ReferensKlass;

public class ÄrBeställda {
    private int varorId;
    private int beställningId;
    private int antalVaror;

    public int getVarorId() {
        return varorId;
    }

    public void setVarorId(int varorId) {
        this.varorId = varorId;
    }

    public int getBeställningId() {
        return beställningId;
    }

    public void setBeställningId(int beställningId) {
        this.beställningId = beställningId;
    }

    public int getAntalVaror() {
        return antalVaror;
    }

    public void setAntalVaror(int antalVaror) {
        this.antalVaror = antalVaror;
    }
}
